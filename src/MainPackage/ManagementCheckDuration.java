    private void getTableData() {
        int CC;
        try {
            pst = con.prepareStatement("SELECT * FROM vehicles ORDER BY duration ASC");
            ResultSet Rs = pst.executeQuery();
            
            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel)jTable1.getModel();
            DFT.setRowCount(0);
            
            while(Rs.next())
            {
                Vector v2 = new Vector();
                for(int ii = 1; ii <= CC; ii++)
                {
                    v2.add(Rs.getInt("idvehicles"));
                    v2.add(Rs.getString("make"));
                    v2.add(Rs.getString("model"));
                    v2.add(Rs.getString("colour"));
                    v2.add(Rs.getString("numberplate"));
                    v2.add(Rs.getInt("duration"));
                }
                DFT.addRow(v2);
           }
        }
        catch (SQLException ex)
        {
           Logger.getLogger(ManagementCheckDuration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

