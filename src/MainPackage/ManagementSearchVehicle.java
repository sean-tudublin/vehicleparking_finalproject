    private void getTableData() {
        int CC;
        try {
            pst = con.prepareStatement("SELECT * FROM vehicles");
            ResultSet Rs = pst.executeQuery();
            
            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel)jTable1.getModel();
            DFT.setRowCount(0);
            
            while(Rs.next())
            {
                Vector v2 = new Vector();
                for(int ii = 1; ii <= CC; ii++)
                {
                    v2.add(Rs.getString("make"));
                    v2.add(Rs.getString("model"));
                    v2.add(Rs.getString("colour"));
                    v2.add(Rs.getString("numberplate"));
                }
                DFT.addRow(v2);
           }
        }
        catch (SQLException ex)
        {
           Logger.getLogger(ManagementSearchVehicle.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    

